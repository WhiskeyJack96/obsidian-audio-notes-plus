import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const isProd = process.argv[2] === "production";

const sharedConfig = {
	bundle: true,
	logLevel: "info",
	sourcemap: isProd ? false : "inline",
	treeShaking: true,
	minify: isProd,
	target: "es2020",
};

// Pass 1: Main plugin bundle (CJS for Obsidian)
const mainContext = await esbuild.context({
	...sharedConfig,
	banner: { js: banner },
	entryPoints: ["src/main.ts"],
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins,
	],
	format: "cjs",
	outfile: "main.js",
});

// Pass 2: Worker bundle (ESM, self-contained, browser platform)
const workerContext = await esbuild.context({
	...sharedConfig,
	entryPoints: ["src/transcription/worker.ts"],
	format: "esm",
	outfile: "worker.js",
	platform: "browser",
	banner: {
		// Shadow Electron's global `process` on the worker's own scope.
		// Both Transformers.js and the external ort-wasm-simd-threaded
		// .mjs files detect Node.js via different checks:
		//   - Transformers.js: process?.release?.name === "node"
		//   - ort .mjs files:  typeof process.versions.node === "string"
		// If either is true, they import Node-only modules (onnxruntime-node,
		// worker_threads) that fail in the blob-URL worker context.
		// Removing `release.name` and `versions` forces both libraries to
		// take the browser code path. This is safe because the worker is an
		// isolated blob-URL context — the main thread's process is unaffected.
		js: `if(typeof process!=="undefined"){self.process={...process,release:{...process.release,name:""},versions:{}};}`
	},
	define: {
		"process.env.NODE_ENV": isProd ? '"production"' : '"development"',
	},
});

if (isProd) {
	await Promise.all([mainContext.rebuild(), workerContext.rebuild()]);
	await Promise.all([mainContext.dispose(), workerContext.dispose()]);
	process.exit(0);
} else {
	await Promise.all([mainContext.watch(), workerContext.watch()]);
}
